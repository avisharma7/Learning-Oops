/*

Run Time Polymorphism, also known as dynamic polymorphism. ex: function overriding
Function overriding::
means  when the child class contains the method which is already present in the parent class.
Hence,  the child class overrides the method of parent class.

In case of function overriding, parent and child classes both contain the same function with different defination.
The call to the function is determined at run time is knows as runtime polymorphism.

*/